package loops.forloop;

public class multiplicationtable {
    public static void main(String[] args) {
        /*1*1=1*/
//create a multiplication table from 1 to 5

        for (int i=1; i<6; i++) {

            for (int k=1; k<11; k++){

                System.out.println(i + " * " + k +" = " +i*k);




            }
        }
    }
}
